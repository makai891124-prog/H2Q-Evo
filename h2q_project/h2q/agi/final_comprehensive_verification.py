def print_sierpinski(n):
    """æ‰“å°Sierpinskiä¸‰è§’å½¢."""
    size = 2 ** n
    for y in range(size):
        print(" " * (size - y - 1), end="")
        for x in range(y + 1):
            if (y & x) == 0:
                print("â–² ", end="")
            else:
                print("  ", end="")
        print()


def print_mandelbrot():
    """æ‰“å°ç®€åŒ–çš„Mandelbroté›†."""
    chars = " .:-=+*#%@"
    
    for y in range(-12, 13):
        line = ""
        for x in range(-39, 40):
            zr, zi = 0, 0
            cr = x / 20.0
            ci = y / 10.0
            
            iterations = 0
            while zr*zr + zi*zi < 4 and iterations < 20:
                zr, zi = zr*zr - zi*zi + cr, 2*zr*zi + ci
                iterations += 1
            
            line += chars[min(iterations, len(chars)-1)]
        print(line)


